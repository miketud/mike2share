# 2Ô∏è‚É£  Ensure Python ‚â•3.8 via pyenv (fallback if system python too old)
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
ensure_python() {
  local min="3.8"
  if command -v python3 >/dev/null; then
    local cur=$(python3 -c 'import sys; print(f"{sys.version_info.major}.{sys.version_info.minor}")')
    if [[ "$(printf '%s\n' "$min" "$cur" | sort -V | head -n1)" != "$min" ]]; then
      echo "‚ö†Ô∏è System python $cur < $min ‚Äì installing pyenv."
    else
      echo "‚úÖ System python $cur OK."
      return
    fi
  fi
  echo "üîß Installing pyenv‚Ä¶"
  curl -fsSL https://pyenv.run | bash
  export PYENV_ROOT="${HOME}/.pyenv"
  export PATH="${PYENV_ROOT}/bin:${PATH}"
  eval "$(pyenv init -)"
  eval "$(pyenv virtualenv-init -)"
  local latest=$(pyenv install -l | grep -E '^\s*3\.[0-9]+\.[0-9]+$' | tail -1 | tr -d ' ')
  pyenv install -s "$latest"
  pyenv global "$latest"
  echo "‚úÖ pyenv Python $latest ready."
}
